public class UsoCicloWhileYDoWhile {

   
    public static void main(String[] args) {
        int i = 0 ;
        
        while ( i <=15){
            System.out.println ("imprimiendo desde ciclo while:" +i);
            i++;
        }
        
        int c = 0;
        
        do {
            System.out.println ("Imprimiendo desde DoWhile :" + c);
            c++;
        } while (c <=15);

    }
    
}
